package main.java;

public class MultipleVariableDeclaration {
    public static void main(String[] args) {
        int a=12,b=13,c=14;

        System.out.println("a value is:"+a);
        System.out.println("b value is:"+b);
        System.out.println("c value is:"+c);

    }

    //multiple variable declaration is allowed in java
    //Advantages - conciseness,readability
    //disadvantages -- readability,mainatainability,increased risk of errors

}
